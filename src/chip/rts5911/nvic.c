//
// I2C2_Handler
//
void I2C2_Handler(void)
{
	uint8_t Data;
	__disable_irq();
	NVIC_DisableIRQ(I2C2_IRQn);
	uint32_t i;

	if((I2C2->INTSTAT & 0x00000020) == 0x00000020) {				/* Check if RD_REQ */
		// Byte read
		if((I2C2->INTSTAT & 0x00000040) != 0)
			i = I2C2->CLRTXABRT;									/* Interrupt Read Clear */
#ifdef CONFIG_HID_DIAL_SLAVE_2
		// a hook for data return to host
		Data = get_hid_report_data_dial(g_i2c_slave_2_byte_read_index);
#endif
		I2C2->DATACMD = ((uint32_t) Data | ((uint32_t) 0x00 << 8));
		i = I2C2->CLRRDREQ; 										/* Interrupt Read Clear */
	
		/* index to next byte of byte read buffer. */
		g_i2c_slave_2_byte_read_index++;
	}

	else if((I2C2->INTSTAT & 0x00000004) == 0x00000004) {			/* Check if RX Full */
		// Byte write
		i = I2C2->CLRINT;											/* Interrupt Read Clear */
		/* get data from master to buffer. */
		g_i2c_slave_2_byte_write_buffer_pointer[g_i2c_slave_2_byte_write_index] = I2C2->DATACMD_b.DATA;

		/* index to next byte of byte write buffer. */
		g_i2c_slave_2_byte_write_index++;
		if (g_i2c_slave_2_byte_write_index > 0x01)
		{

#ifdef CONFIG_HID_DIAL_SLAVE_2
			process_hid_register_dial(g_i2c_slave_2_byte_write_index);
#endif	/* CONFIG_HID_DIAL_SLAVE_2 */
		}
	}
	else if((I2C2->INTSTAT & 0x00000200) == 0x00000200) {			/* Check if detect STOP condition */
		/* reset w/r buffer index */
		I2C_Slave_2_Variables_Reset();
	}
	else {
		// error occur

		/* reset w/r buffer index */
		I2C_Slave_2_Variables_Reset();
#ifdef CONFIG_HID_DIAL
		set_hid_int_high();
		clear_hid_kb_data();
#endif /* CONFIG_HID_DIAL */
	}

	NVIC_GetEnableIRQ(I2C2_IRQn);
	__enable_irq();
}

