//
// EOCHRX_Handler
//
void EOCHRX_Handler(void)
{
	if(OOB_IN_BUF[1] == 0x0F && eRPMCBuf==0)	// eRPMC
	{
		OOBNum = 0;
		if(OOB_IN_BUF[7] == 0x88)
		{
			ESPI->EORXBUF = (volatile uint32_t)OOB_IN_BUF_1;
			eRPMCBuf = 1;
		}
		else
		{
			ESPI->EORXBUF = (volatile uint32_t)OOB_IN_BUF;
			BIT_SET(SERVICEflag,SERVICE_F_ERPMC);
			eRPMCFlag = 1;
		}
	}
	else if(OOB_IN_BUF_1[1] == 0x0F && eRPMCBuf==1 &&
			((eRPMCFlag == 0)||IS_BIT_CLR(SERVICEflag,SERVICE_F_ERPMC)))
	{
		ESPI->EORXBUF = (volatile uint32_t)OOB_IN_EMPTY;
		ESPI->EORXINTEN_b.RXEN = 0;
		BIT_SET(SERVICEflag,SERVICE_F_ERPMC);
		eRPMCFlag = 1;
		OOBNum = 0;
	}
	else if(OOB_IN_BUF[1] == 0x01)				// PECI
	{
		if(OOBNum == DTSNum) {
		/* Check PECI Response	0x40: Command passed. 0x80/0x81: Response timeout. */
			if (OOB_IN_BUF[4] == 0x40) {
				/* Data Valid */
				OOB_PECI_valid = 1;
				CPUTemperature = ((OOB_IN_BUF[6] << 8) | OOB_IN_BUF[5]);
			}	
			else {
				/* Data Invalid */
				OOB_PECI_valid = 0;
			}
		}
		if(OOBNum == RDPKGNum) {
		/* Check PECI Response	0x40: Command passed. 0x80/0x81: Response timeout. */
			if (OOB_IN_BUF[4] == 0x40) {
				/* Data Valid */
				OOB_PECI_valid = 1;
				if (RDPKGIdx == RDPKGIdx_CPUTjMax) {
					CPUTjMax = OOB_IN_BUF[8];
				} 
				else if (RDPKGIdx == RDPKGIdx_CPUTDP) {
					CPUTDP6 = OOB_IN_BUF[6];
					CPUTDP7 = OOB_IN_BUF[7];
				}
			} else {
				/* Data Invalid */
				OOB_PECI_valid = 0;
			}
			RDPKGIdx = 0;
		}
		if(OOBNum == WRPKGNum) {
			if (OOB_IN_BUF[4] == 0x40) {
				/* Data Valid */
				OOB_PECI_valid = 1;
			} 
			else {
				/* Data Invalid */
				OOB_PECI_valid = 0;
			}
		}
		OOBNum = 0;
		OOB_PECI_Done = 1;
	}

	ESPI->EOSTS_b.RXDONE = 1;
}

